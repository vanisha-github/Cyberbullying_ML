# Optuna objective function for hyperparameter tuning (with pruning)
def objective(trial):
    # Suggest hyperparameters
    learning_rate = trial.suggest_categorical("lr", [1e-5, 3e-5, 5e-5])
    batch_size = trial.suggest_categorical("batch_size", [8, 16, 32])
    epochs = trial.suggest_int("epochs", 2, 4)

    # Tokenize train/validation data
    train_tokens = tokenize_texts(train_texts)
    val_tokens = tokenize_texts(val_texts)

    # Wrap into PyTorch datasets and loaders
    train_dataset = BullyDataset(train_tokens, list(train_labels))
    val_dataset = BullyDataset(val_tokens, list(val_labels))
    train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True)
    val_loader = DataLoader(val_dataset, batch_size=batch_size)

    # Load model for binary classification
    model = DistilBertForSequenceClassification.from_pretrained(
        "distilbert-base-uncased",
        num_labels=2
    ).to(device)

    optimizer = AdamW(model.parameters(), lr=learning_rate)
    loss_fn = torch.nn.CrossEntropyLoss()

    # Training loop
    for epoch in range(epochs):
        model.train()
        for batch in train_loader:
            optimizer.zero_grad()
            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            labels = batch['labels'].to(device)

            outputs = model(input_ids=input_ids, attention_mask=attention_mask)
            loss = loss_fn(outputs.logits, labels)
            loss.backward()
            optimizer.step()

        # Validation phase
        model.eval()
        preds, truths = [], []
        with torch.no_grad():
            for batch in val_loader:
                input_ids = batch['input_ids'].to(device)
                attention_mask = batch['attention_mask'].to(device)
                labels = batch['labels'].to(device)

                outputs = model(input_ids=input_ids, attention_mask=attention_mask)
                predictions = torch.argmax(torch.softmax(outputs.logits, dim=1), dim=1)

                preds.extend(predictions.cpu().numpy())
                truths.extend(labels.cpu().numpy())

        f1 = f1_score(truths, preds)  # main metric

        # Report progress to Optuna & prune if needed
        trial.report(f1, epoch)
        if trial.should_prune():
            raise optuna.TrialPruned()

    return f1

