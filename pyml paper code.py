# Function to compute predictions, probabilities, and evaluation metrics
def get_metrics(data_loader):
    preds, truths, probs = [], [], []
    with torch.no_grad():
        for batch in data_loader:
            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            labels = batch['labels'].to(device)

            outputs = model(input_ids=input_ids, attention_mask=attention_mask)
            softmax_probs = torch.softmax(outputs.logits, dim=1)
            predictions = torch.argmax(softmax_probs, dim=1)

            preds.extend(predictions.cpu().numpy())
            truths.extend(labels.cpu().numpy())
            probs.extend(softmax_probs[:, 1].cpu().numpy())  # probability for class 1

    preds, truths, probs = np.array(preds), np.array(truths), np.array(probs)
    metrics = {
        "accuracy": accuracy_score(truths, preds),
        "precision": precision_score(truths, preds),
        "recall": recall_score(truths, preds),
        "f1_macro": f1_score(truths, preds, average='macro'),
        "f1_weighted": f1_score(truths, preds, average='weighted'),
        "roc_auc": roc_auc_score(truths, probs),
        "preds": preds,
        "truths": truths,
        "probs": probs
    }
    return metrics

