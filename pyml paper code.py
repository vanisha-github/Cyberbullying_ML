# A helper for SHAP that takes token IDs, runs them through the model,
# and returns the probability of the "cyberbullying" class (index 1 here)
def pred_on_token_ids(token_id_array):
    input_ids = torch.tensor(token_id_array, dtype=torch.long).to(device)
    attention_mask = (input_ids != tokenizer.pad_token_id).long().to(device)  # mask out padding tokens
    model.eval()
    with torch.no_grad():
        outputs = model(input_ids=input_ids, attention_mask=attention_mask)
        probs = torch.softmax(outputs.logits, dim=1)[:, 1].cpu().numpy()
    return probs

# Pick 10 random validation texts as a "reference" set for SHAP to compare against
