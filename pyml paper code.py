background_tokens = tokenizer(
    background_texts, truncation=True, padding='max_length',
    max_length=128, return_tensors='np'
)
background_input_ids = background_tokens['input_ids']

# Create the SHAP KernelExplainer using our prediction function and reference examples
explainer = shap.KernelExplainer(pred_on_token_ids, background_input_ids)

# Select 50 random validation texts we actually want to explain
test_texts = val_texts.sample(50, random_state=42).tolist()
test_tokens = tokenizer(
    test_texts, truncation=True, padding='max_length',
    max_length=128, return_tensors='np'
)
test_input_ids = test_tokens['input_ids']

# Compute SHAP values in small batches so we don’t run out of memory
batch_size = 5
all_shap_values = []
for i in range(0, len(test_input_ids), batch_size):
    batch_ids = test_input_ids[i:i+batch_size]
    shap_vals_batch = explainer.shap_values(batch_ids, nsamples=100)
    all_shap_values.append(shap_vals_batch)

# Combine all the batches into one array
shap_values = np.vstack(all_shap_values)

