# ---- Settings ----
os.makedirs("lime_outputs", exist_ok=True)  # Create folder to store HTML explanations
explainer = LimeTextExplainer(
    class_names=list(le.classes_),  # Class names from your LabelEncoder
    random_state=42                 # For reproducibility
)

# Ensure validation texts and labels have clean index 0..n-1
texts = val_texts.reset_index(drop=True)
labels_series = val_labels.reset_index(drop=True)

# Define a prediction function for LIME
# It takes a list of texts, tokenizes them, passes them to the model,
# and returns probabilities (n_samples x n_classes)
def predict_proba_fn(texts_list):
    model.eval()  # Set model in evaluation mode
    toks = tokenizer(
        list(texts_list),
        truncation=True,
        padding=True,
        max_length=128,
        return_tensors='pt'
    )
    toks = {k: v.to(device) for k, v in toks.items()}  # Move tensors to GPU/CPU
    with torch.no_grad():  # Disable gradient calculation for faster inference
        outputs = model(**toks)
        probs = torch.softmax(outputs.logits, dim=1).cpu().numpy()
    return probs

# Number of samples from validation set to explain
N = 50
N = min(N, len(texts))  # Ensure we don't sample more than available
np.random.seed(42)      # For reproducibility
sample_indices = np.random.choice(len(texts), N, replace=False)  # Randomly pick indices

# Aggregators for analysis:
# freq_counter — how many times each word appears as a positive contributor
# weighted_counter — sum of contribution weights across samples
freq_counter = Counter()
weighted_counter = Counter()

# Identify index of 'cyberbullying' class from LabelEncoder
target_label_name = 'cyberbullying'
if target_label_name in list(le.classes_):
    target_label_index = int(np.where(le.classes_ == target_label_name)[0][0])
else:
    target_label_index = 0  # Fallback if not found

print(f"Explaining N={N} samples from validation set. Target class '{target_label_name}' index = {target_label_index}")

# Loop through selected samples
for i, idx in enumerate(sample_indices):
    tweet = texts.iloc[idx]                     # The tweet text
    true_idx = int(labels_series.iloc[idx])     # True label index
    true_name = le.inverse_transform([true_idx])[0]  # True label name

    probs = predict_proba_fn([tweet])[0]        # Get prediction probabilities
    pred_idx = int(np.argmax(probs))            # Predicted label index
    pred_name = le.inverse_transform([pred_idx])[0]  # Predicted label name

    # LIME explanation for target class (cyberbullying)
    exp = explainer.explain_instance(
        tweet,
        predict_proba_fn,
        labels=(target_label_index,),  # Only explain for cyberbullying class
        num_features=15,               # Top 15 words in explanation
        num_samples=2000               # Number of perturbed samples LIME will generate
    )

    # Save explanation as HTML for later inspection
    html_path = f"lime_outputs/lime_explanation_sample{i+1}_idx{idx}.html"
    exp.save_to_file(html_path)

    # Collect words with positive contribution towards cyberbullying class
    for word, weight in exp.as_list(label=target_label_index):
        if weight > 0:  # Only count words that increase cyberbullying probability
            freq_counter[word] += 1
            weighted_counter[word] += weight

    # Display first 5 explanations in notebook for quick inspection
    if i < 5:
        print(f"\n[{i+1}/{N}] idx={idx} true={true_name} pred={pred_name} probs={probs}")
        display(exp.show_in_notebook(text=True))
    else:
        # For the rest, just print progress
        if (i+1) % 10 == 0 or i == N-1:
            print(f"[{i+1}/{N}] processed, saved: {html_path}")

# ---- Plot top words by frequency ----
top_k = 25
top_freq = freq_counter.most_common(top_k)
if top_freq:
    words, counts = zip(*top_freq)
    plt.figure(figsize=(8,8))
    sns.barplot(x=list(counts), y=list(words))
    plt.title(f"Top {len(words)} words — frequency in LIME top-features (N={N})")
    plt.xlabel("Count (appeared as positive contributor)")
    plt.tight_layout()
    plt.show()
else:
    print("No positive tokens collected in freq_counter — check num_features / samples or class label.")

# ---- Plot top words by summed positive weights ----
top_weights = weighted_counter.most_common(top_k)
if top_weights:
    words_w, weights = zip(*top_weights)
    plt.figure(figsize=(8,8))
    sns.barplot(x=list(weights), y=list(words_w))
    plt.title(f"Top {len(words_w)} words — sum of LIME positive weights (N={N})")
