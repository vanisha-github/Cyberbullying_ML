# Tokenize text sequences into input IDs & attention masks
def tokenize_texts(texts):
    return tokenizer(
        list(texts),
        truncation=True,      # cut off longer texts
        padding=True,         # pad shorter texts
        max_length=128,       # fixed sequence length
        return_tensors='pt'   # return as PyTorch tensors
    )

# Custom PyTorch dataset for tokenized tweets
class BullyDataset(Dataset):
    def __init__(self, tokens, labels):
        self.tokens = tokens
        self.labels = labels

    def __len__(self):
        return len(self.labels)

    def __getitem__(self, idx):
        return {
            'input_ids': self.tokens['input_ids'][idx],
            'attention_mask': self.tokens['attention_mask'][idx],
            'labels': torch.tensor(self.labels[idx], dtype=torch.long)
        }

